   n = n * 0.9;

   p = Level.DefaultCalcProf(c, n, acc);
  
   local info = Level.GetDeadBodyInfo(Level.FindActor('NIKO'));
   if (info != nil) then
      if (info.accident == true) then
         p = p + 0.1;
      end
   end
   
   local info = Level.GetDeadBodyInfo(Level.FindActor('SHES'));
   if (info != nil) then
